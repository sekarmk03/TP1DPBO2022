    private void tblAuthorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAuthorMouseClicked
        // TODO add your handling code here:
        int row = tblAuthor.getSelectedRow();
        String tblClicked = tblAuthor.getModel().getValueAt(row, 1).toString();
        
        try {
            paneAuthorImg.setVisible(true);
            String sql = "SELECT author_img FROM author WHERE author_nama = '" + tblClicked + "';";
            lblAuthorImg.setIcon(conn.showImage(sql, "author_img"));
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblAuthorMouseClicked

    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
        // TODO add your handling code here:
        int row = tblBuku.getSelectedRow();
        String tblClicked = tblBuku.getModel().getValueAt(row, 1).toString();
        dbConnection conn = new dbConnection();
        
        try {
            paneBukuImg.setVisible(true);
            String sql = "SELECT buku_img FROM buku WHERE buku_judul = '" + tblClicked + "';";
            lblBukuImg.setIcon(conn.showImage(sql, "buku_img"));
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblBukuMouseClicked

