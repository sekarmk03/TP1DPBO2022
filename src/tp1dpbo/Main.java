    private void tbtnSwitchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbtnSwitchActionPerformed
        // TODO add your handling code here:
        if(tbtnSwitch.isSelected()) {
//            tblAuthor.setModel(conn.readTable());
            String q = "SELECT author_nama, author_jumlah_buku FROM author;";
            Object[] theads = {"No.", "Nama", "Jumlah"};
            Object[] columns = {"author_id","author_nama", "author_jumlah_buku"};
            tblAuthor.setModel(conn.readTable(theads, columns, q));

            panelUtama.setVisible(true);
            panelDataAuthor.setVisible(true);
            tblAuthor.setVisible(true);
            paneAuthorImg.setVisible(false);
        } else {
            panelDataBuku.setVisible(true);
            panelDataAuthor.setVisible(false);
            String q = "SELECT buku.buku_judul, buku.buku_penerbit, author.author_nama, buku.buku_desc FROM buku JOIN author ON buku.author_id=author.author_id;";
            Object[] theads = {"No.", "Judul Buku", "Penerbit", "Author", "Deskripsi"};
            Object[] columns = {"buku.buku_id","buku.buku_judul", "buku.buku_penerbit", "author.author_nama", "buku.buku_desc"};
            tblBuku.setModel(conn.readTable(theads, columns, q));
            
            panelDataAuthor.setVisible(false);
            System.out.println("button unselected");
        }
        
    }//GEN-LAST:event_tbtnSwitchActionPerformed

